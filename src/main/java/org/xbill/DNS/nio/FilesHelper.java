package org.xbill.DNS.nio;

import java.io.*;
import java.nio.charset.Charset;

public class FilesHelper {

    public static BufferedReader newBufferedReader(File file, Charset cs) throws FileNotFoundException {
        return new BufferedReader(new InputStreamReader(new FileInputStream(file), cs));
    }
}
